namespace Dobee\Http\Session;

/**
 * Class Session
 * @package Dobee\Http\Session
 */
class Session implements SessionInterface
{
    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $value;

    /**
     * @var string
     */
    private $sessionId;

    /**
     * @var int
     */
    private $expire;

    /**
     * @param $name
     * @param $value
     * @param int $expire
     * @param string $sessionId
     */
    public function __construct(
        $name,
        $value,
        $expire = 0,
        $sessionId = ''
    )
    {
        $this->name = $name;

        $this->value = $value;

        $this->expire = $expire;

        $this->sessionId = $sessionId;
    }

    /**
     * @param $sessionId
     * @return $this
     */
    public function setSessionId($sessionId)
    {
        $this->sessionId = $sessionId;

        return $this;
    }

    /**
     * @return string
     */
    public function getSessionId()
    {
        return $this->sessionId;
    }

    /**
     * @param $name
     * @return $this
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param $value
     * @return $this
     */
    public function setValue($value)
    {
        $this->value = $value;

        return $this;
    }

    /**
     * @return string
     */
    public function getValue()
    {
        return $this->value;
    }

    /**
     * @param $expire
     * @return $this
     */
    public function setExpire($expire)
    {
        $this->expire = $expire;

        return $this;
    }

    /**
     * @return int
     */
    public function getExpire()
    {
        return $this->expire;
    }
}